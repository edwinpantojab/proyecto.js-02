// En JavaScript, los operadores se utilizan para realizar operaciones en variables y valores. A continuación, se presentan los principales tipos de operadores en JavaScript:

// Operadores aritméticos: se utilizan para realizar operaciones matemáticas básicas, como sumar, restar, multiplicar y dividir valores. Algunos ejemplos son:
// Suma (+)
// Resta (-)
// Multiplicación (*)
// División (/)
// Módulo (%)
// Incremento (++)
// Decremento (--)
// Operadores de comparación: se utilizan para comparar valores y devolver un valor booleano (verdadero o falso). Algunos ejemplos son:
// Igualdad (==)
// Desigualdad (!=)
// Igualdad estricta (===)
// Desigualdad estricta (!==)
// Mayor que (>)
// Mayor o igual que (>=)
// Menor que (<)
// Menor o igual que (<=)
// Operadores lógicos: se utilizan para combinar expresiones booleanas y devolver un valor booleano. Algunos ejemplos son:
// AND lógico (&&)
// OR lógico (||)
// NOT lógico (!)
// Operadores de asignación: se utilizan para asignar valores a variables. Algunos ejemplos son:
// Asignación (=)
// Asignación con suma (+=)
// Asignación con resta (-=)
// Asignación con multiplicación (*=)
// Asignación con división (/=)
// Operadores de tipo: se utilizan para obtener el tipo de un valor o variable. Algunos ejemplos son:
// Typeof
// Instanceof
// Operadores bit a bit: se utilizan para realizar operaciones en bits de números enteros. Algunos ejemplos son:
// AND bit a bit (&)
// OR bit a bit (|)
// NOT bit a bit (~)
// XOR bit a bit (^)
// Desplazamiento a la izquierda (<<)
// Desplazamiento a la derecha (>>)
// Desplazamiento a la derecha sin signo (>>>)
// Estos son solo algunos de los operadores disponibles en JavaScript. Cada uno de ellos tiene su propia sintaxis y uso, por lo que es importante conocerlos para poder utilizarlos adecuadamente en tu código.





